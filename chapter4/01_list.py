# List in python
# A list is a collection of items that can be of different data types.
# Lists are mutable, meaning you can change their content.  
# Lists are defined using square brackets [].

# Example of a list
my_list = [1, 2, 3, 4, 5]

# Accessing elements in a list
print("First element:", my_list[0])  # Output: 1
print("Second element:", my_list[1])  # Output: 2
print("Last element:", my_list[-1])  # Output: 5

# Modifying elements in a list
my_list[0] = 10
print("Modified list:", my_list)  # Output: [10, 2, 3, 4, 5]