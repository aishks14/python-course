# Sets in python
# A set is a collection of unique items that are unordered and unindexed.
# Sets are mutable, meaning you can change their content.   
# Sets are defined using curly braces {} or the set() constructor.
# Sets do not allow duplicate items, and they do not maintain the order of elements.    

# Example of a set
my_set = {1, 2, 3, 4, 5}

# Accessing elements in a set
print("Set elements:", my_set)  # Output: {1, 2, 3, 4, 5}

# Empty set
empty_set = set()
print("Empty set:", empty_set)  # Output: set()
# Emptyset is not created using {} as it creates an empty dictionary, not a set.