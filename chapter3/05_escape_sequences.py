# Escape sequences in Python strings
# are special characters that allow you to include characters in a string that would otherwise be difficult or impossible to type directly.
# They are represented by a backslash (\) followed by a character.
# Common escape sequences include:
# \n - Newline
# \t - Tab
# \\ - Backslash
# \' - Single quote
# \" - Double quote
# \r - Carriage return
# \b - Backspace
# \f - Form feed
# \v - Vertical tab
# Escape sequences are useful for formatting strings, especially when dealing with special characters or when you want to include quotes within a string.

# Newline Example
newline_example = "Hello,\nWorld!"

# Tab Example
tab_example = "Hello,\tWorld!"

# Backslash Example
backslash_example = "Hello,\\World!"

# Single Quote Example
single_quote_example = 'Hello, \'World!\''

# Double Quote Example
double_quote_example = "Hello, \"World!\""

# Carriage Return Example
carriage_return_example = "Hello,\rWorld!"

# Backspace Example
backspace_example = "Hello,\bWorld!"

# Form Feed Example
form_feed_example = "Hello,\fWorld!"

# Vertical Tab Example
vertical_tab_example = "Hello,\vWorld!"

# Print all examples
print("\nEscape Sequences Example:")
print("--------------------------")
print("Newline Example:", newline_example)
print("Tab Example:", tab_example)
print("Backslash Example:", backslash_example)
print("Single Quote Example:", single_quote_example)
print("Double Quote Example:", double_quote_example)
print("Carriage Return Example:", carriage_return_example)
print("Backspace Example:", backspace_example)
print("Form Feed Example:", form_feed_example)
print("Vertical Tab Example:", vertical_tab_example)