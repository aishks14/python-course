# Example to print elements of a list using for loop
# This code demonstrates how to print the content of a list using a for loop.
my_list = [1, True, "Python", "Programming", 2, 3, 4, 5]
print("\nPrinting the content of a list using a for loop:")
print("--------------------------------------------------")
for item in my_list:
    print(item)

# Example to print the elements of a tuple using a for loop
my_tuple = (1, 2, 3, 4, 5)
print("\nPrinting the content of a tuple using a for loop:")
print("--------------------------------------------------")
for item in my_tuple:
    print(item)

# Example to print the elements of a string using a for loop
my_string = "Python"
print("\nPrinting the content of a string using a for loop:")
print("----------------------------------------------------")
for item in my_string:
    print(item)
